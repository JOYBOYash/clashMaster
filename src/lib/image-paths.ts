const buildingImageMap: Record<string, Record<number, any>> = {};

const addBuildingImage = (name: string, level: number, image: any) => {
    if (!buildingImageMap[name]) {
        buildingImageMap[name] = {};
    }
    buildingImageMap[name][level] = image;
};

// This is tedious, but it's the only way with static imports
// You would need to add an import for every single building/level combination
// Example for one building:
// import armyCamp1 from '../../public/_buildings/army-camp/Building_HV_Army_Camp_level_1.png';
// addBuildingImage('Army Camp', 1, armyCamp1);

export function getBuildingImagePath(name: string, level: number): any {
    // For now, return a default since we can't statically import all 1000+ building images.
    // The long-term solution would be to use simple string paths with the unoptimized prop.
    const imageName = name.replace(/\s+/g, '_');
    if (imageName.toLowerCase() === 'x-bow') {
        return `/_buildings/x-bow/Building_HV_X-Bow_level_${level}.png`;
    }
     if (imageName.toLowerCase() === 'wall') {
        return `/_buildings/wall/Wall_level_${level}.png`;
    }
    const formattedName = name.replace(/\s+/g, '-').toLowerCase();
    return `/_buildings/${formattedName}/Building_HV_${imageName}_level_${level}.png`;
};

    